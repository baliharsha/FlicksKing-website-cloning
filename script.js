let btn= document.querySelector(".p6btn")
console.log(btn);



    
    
    btn.addEventListener("mouseenter", function(){
    
   btn.classList.add("hover")
   setTimeout(()=>{
    btn.classList.remove("hover")
   },2000)



})

btn.addEventListener("mouseleave", function(){

    btn.classList.remove("hover")


})

